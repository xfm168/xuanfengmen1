let total = 0
let passed = 0
let failed = 0

function test(name: string, condition: boolean, detail?: string) {
  total++
  if (condition) {
    passed++
  } else {
    failed++
    console.log(`  ✗ [${name}] ${detail || ''}`)
  }
  return condition
}

function makeSixLines(
  y: [string, string], m: [string, string],
  d: [string, string], h: [string, string],
) {
  return {
    year: { gan: y[0], zhi: y[1] } as GanZhi,
    month: { gan: m[0], zhi: m[1] } as GanZhi,
    day: { gan: d[0], zhi: d[1] } as GanZhi,
    hour: { gan: h[0], zhi: h[1] } as GanZhi,
  }
}

function fe(c: Partial<Record<FiveElement, number>>): Record<FiveElement, number> {
  return { '木': 0, '火': 0, '土': 0, '金': 0, '水': 0, ...c }
}

function run(
  dayGan: string, monthZhi: string,
  monthShen: ShenShi, score: number,
  opts: {
    y?: [string, string], m?: [string, string],
    d?: [string, string], h?: [string, string],
    shen?: Record<string, ShenShi>,
    fe?: Partial<Record<FiveElement, number>>,
  } = {},
) {
  const y = opts.y || ['甲', '寅']
  const m = opts.m || [dayGan === '甲' ? '乙' : '丙', monthZhi]
  const d = opts.d || [dayGan, '子']
  const h = opts.h || ['丁', '丑']
  const shen = { [m[0]]: monthShen, ...(opts.shen || {}) }
  const six = makeSixLines(y, m, d, h)
  const f = opts.fe || { '木': 2, '火': 2, '土': 1, '金': 1, '水': 2 }
  return determineGeJu(six, shen, score, dayGan, monthZhi, fe(f))
}

